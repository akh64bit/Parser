package vo;

public class DataType
{
	private DataTypeName name;
	private int size;
	public DataTypeName getName()
	{
		return name;
	}
	public void setName(DataTypeName name) {
		this.name = name;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	}
}

